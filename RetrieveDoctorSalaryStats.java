    private void populateFields() {
        try {
            txtMin.setText("$"+rs.getString("min_sal"));
            txtMax.setText("$"+rs.getString("max_sal"));
            double averageSal = rs.getDouble("avg_sal");
            BigDecimal bd = new BigDecimal(averageSal);
            bd = bd.setScale(2, RoundingMode.HALF_UP);
            txtAvg.setText("$"+bd.toString());
            txtTotal.setText("$"+rs.getString("tot_sal"));
        } catch (SQLException ex) {
            Logger.getLogger(UpdateDeletePatient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
